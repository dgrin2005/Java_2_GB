package obstances;
import animals.*;

public class Water implements Obstance {
    private int length;

    public Water(int length) {
        this.length = length;
    }

    public boolean doIt(Animal animal) {
        if (animal instanceof Swimable)
            return ((Swimable) animal).swim(length);
        else
            return false;
    }

    @Override
    public String toString() {
        return this.getClass().getSimpleName() + " (" + length + ")";
    }
}